  <h1>The Loan Trading Guide</h1>

  <p>
    Loaning can sound very fuzzy at first sight. Nevertheless, it’s a very handy
    tool that can improve your trading, especially when you want to be $SOL
    liquid while keeping your favorite NFTs.
  </p>

  <p>
    That said, it is a very risky tool. By giving you more flexibility, it will
    add volatility to your trades. If it’s not used properly, that can ruin all
    your holdings. So, you should use it with extreme caution and think a lot
    before taking a loan. I recommend loans only for experimented traders.
  </p>

  <p>
    Now you are aware of the situation, In this guide, I’m going to tell you how
    borrowing can be used efficiently. I will cover lending to see how to
    manage it properly.
  </p>
  <h2 id="-main-principle-"><strong>Main principle</strong></h2>
  <p>
    A lender proposes an amount of $SOLs to lend to a borrower but asks an NFT as
    collateral in case the borrower can’t pay. It means that if one borrows
    the $SOLs (which are always lower than the NFT Floor price) and don’t pay
    back, the lender gets the NFT instead of the $SOL lent. Most of the time,
    payment default will be like instant selling your NFT at a discounted price.
  </p>
  <p>
    All loans have a limited duration and come with some interest. These
    parameters are set by the lender, The lender earns money through the
    interest. Duration varies between 3 and 21 days, and interest is between 2
    and 3% for 7 days.
  </p>
  <p>
    The interesting point is that if you default, you lose only your NFT, you
    don’t owe anything to anyone.
  </p>
  <h2 id="-platforms-"><strong>Platforms</strong></h2>
  <p><em>What platform to use?</em></p>
  <p>
    There are 3 main platforms to deal with borrowing:
    <a href="https://sharky.fi/">Shark</a>,
    <a href="https://citrus.famousfoxes.com/">Citrus</a> and
    <a href="https://frakt.xyz/">Frakt</a>. The first one is the most used,
    Citrus is the new challenger, and Frakt is the older.
    <a href="https://rain.fi/">Rainfi</a> is another alternative but with less
    volume.
  </p>
  <p>
    Because all of them are quite simple to use and provide extensive guides
    and tutorials, I won’t cover their usage. I will focus on the lending theory
    solely.
  </p>
  <p>
    If you like statistics, the
    <a href="https://app.suipa.xyz/lending/collection">Suipa</a> application
    provides a lot of insights on loans (volumes, defaults, average interest,
    etc.)
  </p>
  <p><em>What are the deal parameters?</em></p>
  <p>A lender sets:</p>
  <ul>
    <li>How much sols he can furnish</li>
    <li>The NFT collection to use as collateral</li>
    <li>The duration (3, 5, 7, 14 or 21 days)</li>
    <li>The interest rate (between 2 and 10% depending on the duration).</li>
  </ul>
  <p><em>How platforms work?</em></p>
  <p>
    They work in a similar way: lenders list deals, and borrowers can pick the
    deals they are interested in. Borrowers can propose deals too and be picked,
    it’s like a classic trading order book (the way deals work is described in
    the next paragraphs).
  </p>
  <p>
    NB: Some offers can be sent privately. They are dealt on a separate chat
    platform, and the lender sets it for a single wallet via one of the lending
    platforms.
  </p>
  <h2 id="-borrowing-"><strong>Borrowing</strong></h2>
  <p><em>When to borrow?</em></p>
  <p>
    Basically, you borrow $SOL when you need liquidity for a trade. It’s often
    for a mint of which the outcome will be known quickly or for investing in
    collections that will likely pump soon (bullish announcement, incoming
    airdrop for collection holders, or similar). In both cases you will have to
    cash out quickly to pay back your loans and avoid activating the collateral
    NFT selling. Loans are mainly for short-term investments.
  </p>
  <p>
    My recommendation is to borrow 30% maximum of your current holding. That
    way, if things go wrong, you will still be able to pay back the borrowing.
    The other option to limit risks is to be ready to lose the NFTs put as
    collateral.
  </p>
  <p>
    Note that loaning can be good for enforcing a trading strategy. They force you
    to cash out your trades at a given time. It avoids holding the NFT with
    a false hope that its floor gets higher.
  </p>
  <p><em>What is a good deal?</em></p>
  <p>
    Interest amount is most of the time not significant compared to the
    expected gains of your upcoming trade. They should be considered but if the
    amount is a problem, it means that the deal is not that good or that there
    is an issue with your strategy.
  </p>
  <p><em>Be liquid</em></p>
  <p>
    Another option to mitigate risks is to spend only the money you borrowed.
    So, you make sure you can pay it back in case of a big loss. You still have
    your liquidity available in case of sudden new opportunities.
  </p>
  <p>
    You are always liquid, meaning you have $SOLs available in your wallet. You
    mainly invest money from others for your short-term trades.
  </p>
  <p><em>Set a stop loss</em></p>
  <p>
    Another usage for borrowing is to set a stop loss on an NFT you are not sure
    of. You bought it at the floor price and bet on the price volatility. To manage
    your risks, you want a bottom exit price. There is no tool for that (except
    AMMs, but they are complicated to use, I will cover them in another guide),
    so you can put the NFT as collateral for a borrowing. It will make you more
    liquid while setting your stop loss. But you will have to pay interest for
    this service if the stop loss is not activated.
  </p>
  <p><em>Warning</em></p>
  <p>
    Be aware of the gambler effect, where you put all your NFTs as collateral and
    lose them one by one after many short-term failed opportunities. It could
    ruin your bag very fast and hurt your mental health.
  </p>
  <p><em>Sum up</em></p>
  <p>
    <strong>Upside</strong>: getting more liquidity, always being liquid, better
    investment strategy with stop-loss, enforce exits.
  </p>
  <p>
    <strong>Downside</strong>: risk of losing NFT if your investments go wrong
    or if you forget to pay back. Possible addiction that could ruin your
    portfolio
  </p>
  <p><em>Exemples from the field</em></p>
  <p>1) Buddles Mint</p>
  <ul>
    <li>
      For the Buddles mint, I had access to WL Phase 1 at 2.5 $SOL and WL Phase 2
      at 2.75 $SOL.
    </li>
    <li>
      I had 2.6 $SOL in my wallet. I made a 3 $SOL loan in seven days and put an
      Helion as collateral.
    </li>
    <li>
      I minted 1 Buddle at Phase 1 and 1 at Phase 2. I was expecting a lot from
      the mint.
    </li>
    <li>
      Finally, it didn’t go as expected, but I was able to sell them at 3.10 and
      3.25.
    </li>
    <li>
      After paying back my interest, it was still a 1 $SOL win. Without the loan
      it would be only a 0.6 $SOL gain. That’s a 40% difference.
    </li>
    <li></li>
  </ul>
  <p>2) The Bastards secondary</p>
  <ul>
    <li>Another example with Bastards. This time it went wrong.</li>
    <li>
      I had 9 $SOL in my wallet. FP was at 11 $SOL. I decided to take a 3 $SOL
      loan by putting a Helion as collateral and was able to buy one. I bought
      a bastard and had 12 $SOL in my wallet
    </li>
    <li>Things went wrong, the FP reached 9 $SOL.</li>
    <li>
      I decided to exit and got 10 $SOL on my wallet. Because I didn’t borrow to
      much, I was able to pay back my loan and got my NFT back and ended at 7
      $SOL. I lost 23% of my initial investment.
    </li>
  </ul>
  <h2 id="-lending-"><strong>Lending</strong></h2>
  <p><em>When to lend?</em></p>
  <p>
    The main benefit of lending is obvious: it allows to get interests from the
    borrower. On average it leads to 200-300% APR, which is huge compared to
    Solana validators (5-10%).
  </p>
  <p>
    To set a lend, you have to decide several parameters: the platform you use,
    the amount of $SOL loaned the duration, the interest rate, the amount lent
    and the NFT is used as collateral.
  </p>
  <p>
    Be aware that the platform will get some fees. Some platform has a better rate
    than others, but overall, they are in a decent range.
  </p>
  <p><em>What deals can you propose?</em></p>
  <p>
    You can look at other offers to be sure of what parameters you should choose.
    Once you set your deal, you just have to do the maths (the platform helps you
    with that) and see if it’s worth the risk for you.
  </p>
  <p>
    The deal has to be a bit far from the floor to avoid payment defaults in case
    the collection dumps a bit. But it must not be too far because it would
    make the loan not interesting for the borrower. On average, proposals are
    between 15 and 20% below the floor. Check the offers made by other users, if
    they are better, they will be selected before yours. Keep in mind that your
    deal must be accepted, it needs to be attractive.
  </p>
  <p>
    The platform represents the above concept as LTV. It’s the percentage of the
    floor price you lend.
  </p>
  <p>
    The other thing you can set is the interest rate, which is usually around
    2.5% for 7 days. The platform will present you with the form of APR, but they
    will do maths with other parameters to show how much interest you gain in
    the end. You can set longer loans too, like 14 days or 21 days, the interest
    rate evolves accordingly.
  </p>
  <p>
    The bottom line is your loan parameter depends on your patience, you are in
    a market with competitors. The more attractive the offer, the faster it goes, but the less the rewards you get.
  </p>
  <p>
    NB: When popular mints occur, a lot of people are looking for liquidity.
    During this period, you can make less attractive offers, like 10% below the floor
    with a stronger interest rate.
  </p>
  <p><em>How to deal with payment default ?</em></p>
  <p>
    The payment default allows you to get an NFT at a discount price. It can occur
    for two reasons:
  </p>
  <ul>
    <li>
      The borrower made bad trades and cannot pay back the loan. That’s a good
      situation, you get an NFT below its floor price.
    </li>
    <li>
      The collection you lent on dumped, and the lender chose to not repay his
      loan, and you get the NFT at a higher price than the current floor price.
    </li>
  </ul>
  <p>
    To avoid the second case, there is no magic recipe. The only thing you can
    do is to give loans only on collections you are ready to hold in case they
    dump. You can focus on solid and stable collections to put the
    probabilities on your side.
  </p>
  <p><em>Sum up</em></p>
  <p>
    <strong>Upside</strong>: getting great APR for $SOL, acquiring NFTs at cheap
    price
  </p>
  <p><strong>Downside</strong>: Can end with an expensive NFT</p>
  <h2 id="mortgages">Mortgages</h2>
  <p><em>What makes mortgages different than loans?</em></p>
  <p>
    Mortgages are leverage tools for borrowers. From the lender's point of view,
    it doesn’t change anything.
  </p>
  <p>
    As a borrower, mortgages allow you to buy an NFT without having the liquidity
    yet. It’s a bet on the fact that a collection will pump soon. You buy a NFT
    with the difference between the floor price and royalties and the best loan
    offer. It allows you to get the NFT right now while you are still missing
    the liquidity to buy it. But the NFT is frozen and can’t be traded until
    you pay the loan back.
  </p>
  <p>
    The loan can be paid via an instant sale of the NFT. So, if things go well
    you can trade out of your range NFT without having the liquidity for this.
    If you default, the initial money is lost, and the NFT is given.
  </p>
  <p>
    That’s still a risky tool. Its main utility is to allow you to buy several.
    NFTs with less money, and it allows you to add a multiplicator to your trades by
    buying more NFTs at the same time. In your prediction, if you go for multiple
    NFTs include the fact that royalty costs are multiplied too.
  </p>
  <p><em>Exemple</em></p>
  <p>Let’s take an example to make things clearer:</p>
  <ul>
    <li>
      At the time of the writing, I can buy an Okay Bear mortgage at 18.5 $SOL
      with a Floor price of 75. NB: If I had 80 $SOL in my wallet I could buy 4
      Bears.
    </li>
    <li>
      If I take a mortgage, the Okay Bear is now in my wallet and gives me holder
      access to the Okay Bear community. But, I got a loan of 61 $SOL, and the
      NFT can’t be traded until I pay the loan.
    </li>
    <li>
      If the price goes up to 95, I can decide to fund the money borrowed,
      unlock the NFT and sell it on the market for a 20 $SOL gain (minus royalties
      and loan interests).
    </li>
    <li>
      If the price goes down to 65, I lose the 10 $SOL (plus royalties and loan
      interests)
    </li>
    <li>If I can’t find the missing liquidity I lose 18.5 $SOL.</li>
    <li>
      If the floor dumps harder, like at 55, I can default my payment and lose
      18.5 $SOL instead of 20.
    </li>
  </ul>
  <p><em>To sum up</em></p>
  <p>
    <strong>Upside</strong>: buying big NFT without being liquid enough, trading
    more NFTs at the same time
  </p>
  <p>
    <strong>Downside</strong>: can lose the initial investment, loss is harder,
    add a layer of complexity on top of the loan
  </p>
  <p><strong>To conclude</strong></p>
  <p>
    Loans are tools to fix the fact that NFTs are not liquid. They allow you to
    have access to your $SOL while holding them. Still, they create more
    volatility in your investment and add more risks to it. In this guide, I
    did my best to give you a full understanding of how they work and how can
    they be helpful if used wisely. I hope it will help, and keep in mind that
    you should always bet money you can afford to lose.
  </p>
