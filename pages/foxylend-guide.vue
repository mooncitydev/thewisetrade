<h2>Lending</h2>


<h3>How it works</h3>

<p>
The lending benefits are much simpler. You are missing elements to send your
Fox directly to a mission, or you prefer a good amount of $FOXYs instead of
chests. So, the simplest thing to do is to lend it to someone for a bunch of
$FOXYs. You will gain experience from the mission, but your fox will be sent
back only when its mission finishes.
</p>


<h3>Chose the right price</h3>

<p>
That’s a good idea, but how to set the right price? There is no magic recipe.
The price range is roughly 1500-4000 $FOXY for a Fox and 400-1700 for a TFF.
If you set a low price, it’s more likely that your Fox will be chosen fast.
But you can try to be at the top of the range, wait longer, and cross your fingers
for your Fox to be borrowed.
</p>

<p>
Set low prices for faster renting and higher prices for more but riskier rewards.
</p>


<p>
NB: We saw in the borrow guide that the best outcome for a borrower is to
complete an x2 mission. If there are no x2 missions, you should lower your
lending expectations.
</p>


<h3>Warning</h3>

<p>
Don’t try to rent a fox for a free price just to get the extra fame. Some users
will punish you by renting it and removing it from the market.
</p>


<h2>Conclusion</h2>

<p>
That’s it! As you understand, Foxylend can really improve your passive income.
It should be considered if you want to level up your Fox faster and want to
be able to buy its upgrades. Done properly, it will allow you to get extra fame
or extra chests at a good price.
</p>

