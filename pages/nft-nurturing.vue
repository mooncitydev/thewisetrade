  <h1>The NFT Nurturing guide</h1>

  <h2>The emotional buying</h2>

<p>
In a time of bear cycle, it's common to hold NFTs bought with a strong
conviction. During this period, all NFTs look the same and don't have a
particular value over others. If you get a rare one, you won't sell it much
above the floor.
</p>

<p>
On the opposite, during a bull cycle, NFTs with a special thing tends to be
sold at a higher price. Emotion takes a bigger role, and it's easier to deal for
higher price, especially via direct sales made through Discord. This 1/1
transaction are very tensed. It's hard to define a right market value. Some
factors can give you hints, but in the end the price is set by what the buyer
is ready to put.
</p>

<p>
This is where I get to my point. During bear markets, it's a good idea to
take advantage of a low FP to find some under the radar gems. 1/1 or super rare
NFTs are still convoited and require significant investment. But some element
of the collection are worth interest and are sold at floor price.
</p>

<p class="italic">
NB: Rare and popular traits that still sit a higher price can be negotiated for
better deals.
</p>

  <h2>Nurture your NFT</h2>

<p>
Once you collected your NFT of interest, you will have to hold it until the
next bull phase to propose it at a much better price. It's already a good
strategy that can be enhanced by pushing your investment in this special NFT.
By improving this NFT, you can make it even more desirable. This where NFT
nurturing comes into play.
</p>

<p>
The idea of nurturing is to add perks to your initial NFT. Acquiring it will
come with a bunch of surprises that will delight the buyer. See it as marketing
packaging. During the process you will enjoy crafting a better experience for
the future NFT holder.
</p>

  <h2>Chose your NFT</h2>

<p>
To accomplish this, you need to first find a good NFT. My favorite play is to
look for clean NFTs. They often have less traits and have a low rarity level.
  But they work super well as a PFP: they always caught the attention of the
community and stand out in the marketplace listings.
</p>

<p>
Another great option is to look for rare traits that are undervalued. Let's
take the Tensorians example. The Hoodie Hooded trait is popular because
founders use it as PFPs. The floor sits at the double or triple of the floor
price. During bear cycles, people may be tempted to sell it for a cheaper.
  That's an opportunity to no miss!
</p>

<p>
Now you have find your underrated NFT, it's time to nurture it.
</p>

  <h2>Do your chores</h2>

<p>
The first obvious thing to do is to do all the chores required by the
collection. For instance with the Famous Fox Federation, you have a system of
mission and leveling. So, as example, if you go for a FFF, make sure to do all
the missions available. Claynosaurz has a class system. Make sure you Clayno
get his class.
</p>
<p>
Every points earned, every special attribute gained, are good things for the
future. Remember that you will probably have to negotiate through DMs. For that
you will have to build a compelling offer for your buyer. Think about the box
around a jewel. It makes a ring look totally different. Providing perks like
good leveling to an already special NFT, will make it even more attractive.
</p>

  <h2>Augment your NFT</h2>

<p>
The next step will be to augment your NFT. You are not limited to what the
collection proposes. You can bring value to your NFT without waiting for the
collection owner to make it better.
</p>
<p>
The first thing you can do is to identify an artist and
ask him to do a comission for you by creating an alternative version of your
NFT. So anyone buying your PFP will get 2 visuals for the price of 1. It's my
favorite improvement. It's fun to do, requires little effort on your side and
fund artists on Solana. Whatever happens next, you will be satisfied.
</p>
<p>
Now you understand you can imagine anything. You can create a lore around your
character, make a website for it or model a 3D avatar from it that you can use
in metaverses like Portal. And last but not least, you can sell merch around your
NFT. For instance, the BAYC holders even create companies around the IP of
their ape.
</p>
<p>
I'm not saying that you must do crazy stuff but any perks around your NFT will
make it appealing. When someone will fall in love with your NFT, if you propose
a big markup over 50% above the floor, it will require the best experience
possible. It's a story of collectible and anything that makes your
piece of art brighter will help.
</p>

