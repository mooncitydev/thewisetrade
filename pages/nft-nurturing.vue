  <h2>Final notes</h2>

<p>
Through our three examples, we saw how the value of a single NFT can be
increased via additional work. By adding features to your holding you will
  make it more attractive when the NFT market will be more active.
</p>

<p>
This strategy is very enjoying and can lead to beaufil returns, rewarding your
work and your convictions. But keep in mind that it will lead you to overcommit
on a collection. Unfortunately, your dedication doesn't prevent the collection
to go to the garbage. So make sure to do it only with strong collections with a
 proven model.
</p>

<p>
Overall, it's a risky strategy, because it's a "all or nothing
approach". It requires some design skills and a good sense of negotiaton. So, as
usual put only the money you can afford to lose. In that case, you will enjoy
to make your NFT shinier and eventually take some profits!
</p>

